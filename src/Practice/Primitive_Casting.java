package Practice;

public class Primitive_Casting {
	
	public static void main(String[] args) {
		System.out.println("Implicit");
		int a=10;
		long s=a;
		System.out.println(s);
		
		
	    System.out.println("Explicit");
	    long b=10;
	    int d=(int)b;
	    System.out.println(b);
	}

}
